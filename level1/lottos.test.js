import { expect, test } from "@jest/globals";
import lottos from "./lottos";

test("[44, 1, 0, 0, 31, 25]	[31, 10, 45, 1, 6, 19]	[3, 5]", () => {
  expect(lottos([44, 1, 0, 0, 31, 25], [31, 10, 45, 1, 6, 19])).toEqual([3, 5]);
});
test("[0, 0, 0, 0, 0, 0]	[31, 10, 45, 1, 6, 19]	[3, 5]", () => {
  expect(lottos([0, 0, 0, 0, 0, 0], [31, 10, 45, 1, 6, 19])).toEqual([1, 6]);
});
test("[31, 10, 45, 1, 6, 19]	[31, 10, 45, 1, 6, 19]	[3, 5]", () => {
  expect(lottos([31, 10, 45, 1, 6, 19], [31, 10, 45, 1, 6, 19])).toEqual([
    1, 1,
  ]);
});
